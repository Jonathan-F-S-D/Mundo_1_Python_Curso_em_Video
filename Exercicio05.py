# Exercício 5 Faça um programa que leia um número inteiro e mostre na tela
# o seu sucessor e seu antecessor.

n1 = int(input('Digite um valor para mostrar-lhes seu sucessor e seu antecessor: '))
print('O valor posto é este: {}. Logo, seu antecessor é {} e seu sucessor é {}.' .format(n1, n1-1, n1+1))


